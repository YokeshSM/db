package Yokesh.controller;

public class shop {
    private int id;
    private int b_id;
    private int pr_id;
    private int count;
    private int price;
public int getId() {
    return id;
}
public void setId(int id) {
    this.id = id;
}
public int getB_id() {
    return b_id;
}
public void setB_id(int b_id) {
    this.b_id = b_id;
}
public int getPr_id() {
    return pr_id;
}
public void setPr_id(int pr_id) {
    this.pr_id = pr_id;
}
public int getCount() {
    return count;
}
public void setCount(int count) {
    this.count = count;
}
public int getPrice() {
    return price;
}
public void setPrice(int price) {
    this.price = price;
}
}
